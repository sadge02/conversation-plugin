package minecraft.conversationPlugin.components.trigger.entity.types;

/**
 * Types of entity actions.
 * <ul>
 *     <li>INTERACT: Player interacts with entity</li>
 *     <li>DAMAGE: Entity takes damage</li>
 *     <li>ELIMINATE: Entity is killed</li>
 * </ul>
 */
public enum EntityActionType {

    INTERACT,
    DAMAGE,
    ELIMINATE,
}
