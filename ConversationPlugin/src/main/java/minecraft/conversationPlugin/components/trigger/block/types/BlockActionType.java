package minecraft.conversationPlugin.components.trigger.block.types;

/**
 * Types of block actions.
 * <ul>
 *     <li>INTERACT: Player interacts with block.</li>
 *     <li>PLACE: Player places block.</li>
 *     <li>BREAK: Player breaks block.</li>
 * </ul>
 */
public enum BlockActionType {

    INTERACT,
    PLACE,
    BREAK,
}
