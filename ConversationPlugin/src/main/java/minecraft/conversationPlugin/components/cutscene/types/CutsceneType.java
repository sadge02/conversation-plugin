package minecraft.conversationPlugin.components.cutscene.types;

/**
 * Types of cutscenes.
 * <ul>
 *     <li>NONE: No cutscene.</li>
 *     <li>CUTSCENE: Puts player in a third person cutscene.</li>
 *     <li>PLAYER: Lock player view in first person.</li>
 * </ul>
 */
public enum CutsceneType {

    NONE,
    CUTSCENE,
    PLAYER,
}
